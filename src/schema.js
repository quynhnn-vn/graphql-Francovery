const { gql } = require("apollo-server");

const typeDefs = gql`
  type Query {
    photos(location: String!): AutogeneratedMainType
    articles(location: String!): AutogeneratedMainType
    weather(location: String!): AutogeneratedMainType
  }
  type AutogeneratedMainType {
    results: [Results]
    articles: [Articles]
    list: [List]
  }
  type Results {
    id: ID
    user: User
    urls: Urls
  }
  type Urls {
    regular: String
    full: String
  }
  type User {
    id: String
    username: String
    portfolio_url: String
    profile_image: ProfileImage
  }
  type ProfileImage {
    small: String
  }
  """News API""" 
  type Articles {
    title: String
    description: String
    url: String
    urlToImage: String
    publishedAt: String
    content: String
    source: Source
  }
  type Source {
    name: String
  }

  """Openweather API"""
  type Wind {
    speed: Float
  }
  
  type Clouds {
    all: Int
  }
  
  type Weather {
    main: String
    description: String
    icon: String
  }
  
  type Main {
    temp: Float
    feels_like: Float
    temp_min: Float
    temp_max: Float
    humidity: Int
  }
  
  type List {
    dt_txt: String
    wind: Wind
    clouds: Clouds
    weather: [Weather]
    main: Main
  }
`;

module.exports = typeDefs;
