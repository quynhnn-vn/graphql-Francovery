const { gql } = require("apollo-server");

const typeDefs = gql`
  type Query {
    photos(location: String!): AutogeneratedMainType
  }
  type AutogeneratedMainType {
    results: [Results]
  }
  type Results {
    id: ID
    user: User
    urls: Urls
  }
  type Urls {
    full: String
  }
  type User {
    id: String
    username: String
    portfolio_url: String
    profile_image: ProfileImage
  }
  type ProfileImage {
    small: String
  }
`;

module.exports = typeDefs;
